    @Transactional
    public AssistanceReview createReview(PoputkaUser reviewer, Long requestId, Long revieweeId,
            Integer rating, String comment) {
        AssistanceRequest request = assistanceRequestRepository.findById(requestId)
                .orElseThrow(() -> new IllegalArgumentException("Запрос не найден"));

        // Проверка, что пользователь является участником запроса
        boolean isParticipant = Objects.equals(request.getAuthor().getId(), reviewer.getId()) ;
        //TODO Сделать нормально тут  !!!!!
//                ||
//                (request.getOffers().stream()
//                         .anyMatch(offer -> offer.getHelper().getId().equals(reviewer.getId())));

        if (!isParticipant) {
            throw new IllegalStateException("Только участники запроса могут оставлять отзывы");
        }

        // Проверка, что пользователь не оставляет отзыв самому себе
        if (Objects.equals(reviewer.getId(), revieweeId)) {
            throw new IllegalStateException("Нельзя оставить отзыв самому себе");
        }

        // Проверка существующего отзыва
        Optional<AssistanceReview> existingReview = assistanceReviewRepository
                .findByRequestIdAndCreatedUserAndRevieweeId(requestId, reviewer, revieweeId);

        if (existingReview.isPresent()) {
            throw new IllegalStateException("Вы уже оставили отзыв для этого пользователя по данному запросу");
        }

        // Создание отзыва
        AssistanceReview review = new AssistanceReview();
        review.setRequest(request);
        review.setReviewee(request.getAuthor()); // TODO: Получить реального реципиента отзыва
        review.setRating(rating);
        review.setComment(comment);
        review.setIsAutoGenerated(false);
        review.setIsModerated(false);

        review = assistanceReviewRepository.save(review);

        // Пересчет рейтинга пользователя
        userRatingService.recalculateUserRating(review.getReviewee());

        // Проверка, нужно ли блокировать пользователя
        checkUserBlocking(review.getReviewee());

        return review;
    }
