    public Page<AssistanceReview> getUserReviews(PoputkaUser user, int page, int size) {
        Pageable pageable = PageRequest.of(page, size, Sort.by(Sort.Direction.DESC, "createdAt"));
        return assistanceReviewRepository.findValidReviewsByReviewee(user, pageable);
    }

    public Double getUserAverageRating(PoputkaUser user) {
        return assistanceReviewRepository.calculateAverageRating(user);
    }

    @Transactional
    public AssistanceReview moderateReview(PoputkaUser moderator, Long reviewId, boolean isValid,
            String moderatorComment) {
        AssistanceReview review = assistanceReviewRepository.findById(reviewId)
                .orElseThrow(() -> new IllegalArgumentException("Отзыв не найден"));

        // TODO: Проверка прав модератора

        review.setIsModerated(true);
        review.setModeratorComment(moderatorComment);

        review = assistanceReviewRepository.save(review);

        // Если отзыв признан недействительным, пересчитываем рейтинг
        if (!isValid) {
            userRatingService.recalculateUserRating(review.getReviewee());
            checkUserBlocking(review.getReviewee());
        }

        return review;
    }

    private void checkUserBlocking(PoputkaUser user) {
        Double averageRating = getUserAverageRating(user);

        if (averageRating != null && averageRating < 3.0) {
            user.setRequiresModeration(true);

            // Блокировка создания запросов и откликов
            // TODO: Реализовать блокировку на уровне сервисов

            // Уведомление модераторам
            notificationService.sendNotificationToModerators(
                    "LOW_RATED_USER",
                    "Пользователь с низким рейтингом требует модерации",
                    user.getId()
                                                            );
        }
    }

